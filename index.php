<div class="rectangle" id="1">
    
    <h3> What is Trichophagia?</h3>
    <p>Trichophagia is the compulsive eating of hair associated with <a href="#trich"> trichotillomania (hair pulling)</a>. In trichophagia, people with trichotillomania also ingest the hair that they pull; in extreme (and rare) cases this can lead to a hair ball (trichobezoar)</p>

    <h3 id="trich"> Trichotillomania (hair pulling disorder)</h3><p id="p2"> </p>
    <p> <b>Trichotillomania, also known as trich, is when someone can't resist the urge to pull out their hair.</b></p>
    <p>They may pull out the hair on their head or in other places, such as their eyebrows or eyelashes.</p>
    <p>Trich is more common in teenagers and young adults, and tends to affect girls more often than boys</p>
    <h4>Symptoms of trichotillomania</h4>
    <p>People with trich feel an intense urge to pull their hair out and they experience growing tension until they do. After pulling their hair out, they feel a sense of relief.</p>
    <p>A person may sometimes pull their hair out in response to a stressful situation, or it may be done without really thinking about it.</p>
    <p>
        Most people with trich pull out hair from their scalp, but some pull out hair from other areas, such as their:
        <ul>
            <li>eyebrows</li>
            <li>eyelashes</li>
            <li>genital area</li>
            <li>beard or moustache</li>


        </ul>
        Bald patches left on the head tend to have an unusual shape and may affect one side more than the other.
    </p>

    <p>Trich may cause feelings of shame and low self-esteem. Those affected may try to keep their condition to themselves.</p>
    <h4>Causes of trichotillomania</h4>
    <p>
        It's not entirely clear what causes trich. It could be:
        <ul>
            <li>your way of dealing with stress or anxiety</li>
            <li>a chemical imbalance in the brain, similar to obsessive compulsive disorder (OCD)</li>
            <li>changes in hormone levels during puberty</li>
            <li>a type of self-harm to seek relief from emotional distress</li>

        </ul>
        For some people, hair pulling can be a type of addiction. The more they pull their hair out, the more they want to keep doing it.
    </p>
    <h4>When to see a GP</h4>
    <p>See your GP if you're pulling your hair out or if you notice that your child is.</p>
    <p>You should also see your GP if you or your child has a habit of eating hair. This can cause hairballs to form in the stomach, leading to serious illness.</p>
    <p>Your GP may examine areas where the hair is missing to check that nothing else is causing the hair to come out, such as a skin infection.</p>
    <p>If your GP thinks you have trich, you may be referred for a type of treatment called cognitive behavioural therapy (CBT).</p>
    <h4>Treating trichotillomania</h4>
    <p>Trich is commonly treated using a type of CBT called habit reversal training.  </p>
    <p>
        This aims to help you replace a bad habit with something that's not harmful. Treatment usually involves:
        <ul>
            <li>keeping a diary of your hair pulling</li>
            <li>working out the triggers for your hair pulling and learning how to avoid them</li>
            <li>replacing hair pulling with another action, like squeezing a stress ball</li>
            <li>involving loved ones to provide emotional support and encouragement</li>

        </ul>
        Antidepressants are no longer considered to be an effective treatment for trich.
    </p>
    <h4>Things you can try yourself</h4>
    <p>
        Here are some tips from people with trich that may help when you feel the urge to pull your hair:
        <ul>
            <li>squeeze a stress ball or something similar</li>
            <li>form a ball with your fist and tighten the muscles in that arm</li>
            <li>use a fidget toy</li>
            <li>wear a bandana or a tight-fitting hat, such as a beanie</li>
            <li>come up with a saying that you repeat out loud until the urge to pull passes</li>
            <li>take a soothing bath to ease any stress or anxiety</li>
            <li>practise deep breathing until the urge to pull goes away</li>
            <li>exercise</li>
            <li>put plasters on your fingertips</li>
            <li>cut your hair short</li>

        </ul>

    </p>
    <h4>Getting support </h4>
    <p>It may also help to open up about your trich to people you trust, as hiding it can sometimes make your anxiety worse.</p>
    <p>Many people who have learned to manage their trich say that speaking to others about the condition led to a reduction in hair pulling.</p>
    <p id="source"><small>Source: NHS</small></p>

